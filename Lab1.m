% getting the data
A = input("Enter matrix A: ");
B = input("Enter matrix B: ");
% sum of matrices
Sum_AB = A+B
% difference of matrices
Sub_AB = A-B
% product of matrices
Product_AB = A.*B
% dividing two matrices
Div_AB = A./B
% transpose of a matrix
Transpose_A = A'
% minimum of a matrix
Min_A = min(A)
% maximum of a matrix
Max_A = max(A)
% union of two matrices
Union_AB = union(A,B)
% intersection of two matrices
Inters_AB = intersect(A,B)
% complement of the two matrices
Comp_A = 1-A
Comp_B = 1-B